    private void bTLogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bTLogarActionPerformed
        
        String nome_selecionado  = campoNome.getText();
        String senha_selecionado =  campoSenha.getText();
        Model_funcionario Dados = new Model_funcionario();
        Dados.nome =  nome_selecionado;
        Dados.senha = senha_selecionado;
        
        
            Model_funcionario Resultados = controller.logar(Dados);
            
            
            
     
            if(Resultados.nome != null){
            txtResultado.setText("Logado com sucesso");
            TelaPrincipal p = new TelaPrincipal();
            p.setTitle("Tela Principal");
            p.setLocationRelativeTo(null);
            p.setVisible(true);
            }else{
            txtResultado.setText("Falha ao logar");
            txtResultado.setVisible(true);
            }
        
        
    }//GEN-LAST:event_bTLogarActionPerformed

